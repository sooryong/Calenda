# 합성 레코드의 수신시각을 분산(요일 다양화) — 고정 베이스에 인덱스로 시/일 변주.
BASE_DAYS = ["2026-06-01", "2026-06-02", "2026-06-03", "2026-05-28", "2026-05-29", "2026-05-30"]


def make(records: list[dict], prefix: str) -> list[dict]:
    out = []
    for i, r in enumerate(records):
        out.append({
            "scenario_id": f"{prefix}_{i:03d}",
            "received_at": r["received_at"],
            "channel": "kakao",
            "sender": r["sender"],
            "language": "ko",
            "message": r["message"],
            "gold": dict(NEG),
        })
    return out


def make_synth(pairs: list[tuple[str, str]], prefix: str) -> list[dict]:
    out = []
    for i, (sender, msg) in enumerate(pairs):
        day = BASE_DAYS[i % len(BASE_DAYS)]
        hour = 8 + (i % 12)  # 08~19시 분산
        out.append({
            "scenario_id": f"{prefix}_{i:03d}",
            "received_at": f"{day}T{hour:02d}:{(i * 7) % 60:02d}:00+09:00",
            "channel": "kakao",
            "sender": sender,
            "language": "ko",
            "message": msg,
            "gold": dict(NEG),
        })
    return out


def write(path: str, rows: list[dict]):
    Path(path).parent.mkdir(parents=True, exist_ok=True)
    with open(path, "w", encoding="utf-8") as f:
        for r in rows:
            f.write(json.dumps(r, ensure_ascii=False) + "\n")


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--apply", action="store_true")
    args = ap.parse_args()

    golden = make(GOLDEN, "ad_gold") + make(GOLDEN_TXN, "txn_gold")
    synth = make_synth(SYNTH, "ad_neg") + make_synth(TXN, "txn_neg")

    print(f"골든 음성(실캡처 docs: 광고 {len(GOLDEN)} + 거래알림 {len(GOLDEN_TXN)}): {len(golden)}")
    print(f"학습 부스트(합성: 광고 {len(SYNTH)} + 거래알림 {len(TXN)}): {len(synth)}")
    print("샘플 발신자:", ", ".join(r["sender"] for r in synth[:6]), "...")

    if args.apply:
        write("data/eval/ad_negative.jsonl", golden)
        write("data/processed/ad_negative.jsonl", synth)
        print("→ data/eval/ad_negative.jsonl (골든 음성)")
        print("→ data/processed/ad_negative.jsonl (학습 부스트)")
    else:
        print("(미리보기 — --apply 로 기록)")


if __name__ == "__main__":
    main()

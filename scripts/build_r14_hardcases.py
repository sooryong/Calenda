BASE_DAYS = ["2026-06-01", "2026-06-02", "2026-06-03", "2026-05-28", "2026-05-29", "2026-05-30"]


def make_neg(pairs: list[tuple[str, str]], channel: str, prefix: str) -> list[dict]:
    out = []
    for i, (sender, msg) in enumerate(pairs):
        day = BASE_DAYS[i % len(BASE_DAYS)]
        hour = 8 + (i % 12)
        out.append({
            "scenario_id": f"{prefix}_{i:03d}",
            "received_at": f"{day}T{hour:02d}:{(i * 7) % 60:02d}:00+09:00",
            "channel": channel,
            "sender": sender,
            "language": "ko",
            "message": msg,
            "gold": dict(NEG),
        })
    return out


def make_deadline(records: list[tuple], prefix: str) -> list[dict]:
    out = []
    for i, (received_at, sender, msg, title, date) in enumerate(records):
        ev = {
            "title": title, "date": date, "time": None, "end_time": None,
            "all_day": True, "location": None, "attendees": [], "organizer": None,
            "description": None, "recurrence": None, "confidence": 0.9,
        }
        out.append({
            "scenario_id": f"{prefix}_{i:03d}",
            "received_at": received_at,
            "channel": "sms",
            "sender": sender,
            "language": "ko",
            "message": msg,
            "gold": {"has_schedule": True, "events": [ev]},
        })
    return out


def write(path: str, rows: list[dict]):
    Path(path).parent.mkdir(parents=True, exist_ok=True)
    with open(path, "w", encoding="utf-8") as f:
        for r in rows:
            f.write(json.dumps(r, ensure_ascii=False) + "\n")


